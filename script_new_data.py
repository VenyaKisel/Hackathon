# –°–æ—Ö—Ä–∞–Ω—è–µ–º –Ω–æ–≤—ã–π CSV —Ñ–∞–π–ª
output_filename = 'rations_with_acids.csv'
df.to_csv(output_filename, index=False, encoding='utf-8-sig')

print(f"‚úÖ –°–æ–∑–¥–∞–Ω —Ñ–∞–π–ª {output_filename}")
print(f"üìä –î–æ–±–∞–≤–ª–µ–Ω–æ {len(TARGET_LIMITS)} –∫–æ–ª–æ–Ω–æ–∫ —Å –∫–∏—Å–ª–æ—Ç–∞–º–∏")
print("üìã –°–ø–∏—Å–æ–∫ –¥–æ–±–∞–≤–ª–µ–Ω–Ω—ã—Ö –∫–æ–ª–æ–Ω–æ–∫:")
